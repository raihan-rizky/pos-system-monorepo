import type { ResourceAction } from "@/features/rbac/helpers/rbac-core";
import type { AssistantWorkflowPayload, AssistantWorkflowStepPayload } from "../types/assistant";

export type AssistantWorkflowRequirement = {
  resource: string;
  action: ResourceAction;
};

export type AssistantWorkflowDefinition = AssistantWorkflowPayload & {
  faqNumber: number;
  aliases: string[];
  requiredPages: string[];
  requiredCapabilities: AssistantWorkflowRequirement[];
  iconKey: string;
  steps: Required<AssistantWorkflowStepPayload>[];
};

type WorkflowInput = {
  faqNumber: number;
  slug: string;
  title: string;
  aliases: string[];
  route: string;
  actionLabel: string;
  iconKey: string;
  requiredPages?: string[];
  requiredCapabilities: AssistantWorkflowRequirement[];
  steps: Array<{ title: string; description: string }>;
};

function sourceAnchor(title: string) {
  return title
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, "")
    .trim()
    .replace(/\s+/g, "-");
}

function workflow(input: WorkflowInput): AssistantWorkflowDefinition {
  const id = `faq-q${String(input.faqNumber).padStart(2, "0")}-${input.slug}`;
  return {
    id,
    faqNumber: input.faqNumber,
    title: input.title,
    aliases: input.aliases,
    requiredPages: input.requiredPages ?? [input.route],
    requiredCapabilities: input.requiredCapabilities,
    route: input.route,
    actionLabel: input.actionLabel,
    sourceRef: `docs/help/faq.md#q${input.faqNumber}-${sourceAnchor(input.title)}`,
    iconKey: input.iconKey,
    steps: input.steps.map((step, index) => ({
      id: `${id}-step-${index + 1}`,
      title: step.title,
      description: step.description,
      route: input.route,
      actionLabel: input.actionLabel,
      iconKey: input.iconKey,
    })),
  };
}

